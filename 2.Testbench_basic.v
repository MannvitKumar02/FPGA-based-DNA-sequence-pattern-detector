// Testbench: Basic SequenceDetector (Inline Sequence)

module Testbench;
    reg clk;
    reg rst;
    reg [6:0] dna_char;
    wire [4:0] match_count;

    reg [125:0] target = {
        7'b1000001, 7'b1010100, 7'b1010100, 7'b1010100,
        7'b1000011, 7'b1000011, 7'b1010100, 7'b1010100,
        7'b1010100, 7'b1010100, 7'b1000001, 7'b1000001,
        7'b1000001, 7'b1000001, 7'b1000111, 7'b1000111,
        7'b1000111, 7'b1000001
    };

    reg [699:0] dna_sequence = {
        7'b1000001, 7'b1000011, 7'b1010100, 7'b1000001, 7'b1000001, 7'b1010100, 7'b1010100, 7'b1000001, 7'b1010100, 7'b1010100,
        7'b1010100, 7'b1010100, 7'b1000001, 7'b1000001, 7'b1000001, 7'b1000001, 7'b1000111, 7'b1000111, 7'b1000111, 7'b1000001,
        7'b1000001, 7'b1010100, 7'b1010100, 7'b1010100, 7'b1000011, 7'b1000011, 7'b1010100, 7'b1010100, 7'b1010100, 7'b1010100,
        7'b1000001, 7'b1000001, 7'b1000001, 7'b1000001, 7'b1000111, 7'b1000111, 7'b1000111, 7'b1000001,
        7'b1000001, 7'b1000011, 7'b1010100, 7'b1000001, 7'b1000001, 7'b1010100, 7'b1010100, 7'b1000001, 7'b1010100, 7'b1010100,
        7'b1000001, 7'b1010100, 7'b1010100, 7'b1010100, 7'b1000011, 7'b1000011, 7'b1010100, 7'b1010100, 7'b1010100, 7'b1010100,
        7'b1000001, 7'b1000001, 7'b1000001, 7'b1000001, 7'b1000111, 7'b1000111, 7'b1000111, 7'b1000001,
        7'b1000001, 7'b1000011, 7'b1010100, 7'b1000001, 7'b1000001, 7'b1010100, 7'b1010100, 7'b1000001, 7'b1010100, 7'b1010100,
        7'b1000111, 7'b1000111, 7'b1010100, 7'b1010100, 7'b1000001, 7'b1010100, 7'b1000011, 7'b1000111, 7'b1010100, 7'b1000001,
        7'b1000111, 7'b1000111, 7'b1010100, 7'b1010100, 7'b1000001, 7'b1010100, 7'b1000011, 7'b1000111, 7'b1010100, 7'b1000001,
        7'b1010100, 7'b1000011, 7'b1000111, 7'b1010100
    };

    SequenceDetector detector (
        .clk(clk),
        .rst(rst),
        .dna_char(dna_char),
        .target(target),
        .match_count(match_count)
    );

    always #5 clk = ~clk;

    initial begin
        clk = 0;
        rst = 1;
        dna_char = 7'b0;
        #5 rst = 0;
        for (integer i = 0; i < 100; i = i + 1) begin
            dna_char = dna_sequence[i*7 +: 7];
            #10;
        end
        #1000;
        $finish;
    end
endmodule
